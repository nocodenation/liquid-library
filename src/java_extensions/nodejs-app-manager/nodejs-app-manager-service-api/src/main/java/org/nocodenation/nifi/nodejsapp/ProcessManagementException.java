package org.nocodenation.nifi.nodejsapp;

/**
 * Exception thrown when Node.js process management operations fail.
 *
 * This exception is used to signal failures during process lifecycle operations
 * such as starting, stopping, restarting, or monitoring the Node.js application.
 */
public class ProcessManagementException extends Exception {

    /**
     * Creates a new ProcessManagementException with the specified detail message.
     *
     * @param message the detail message explaining the failure
     */
    public ProcessManagementException(String message) {
        super(message);
    }

    /**
     * Creates a new ProcessManagementException with the specified detail message
     * and cause.
     *
     * @param message the detail message explaining the failure
     * @param cause the underlying cause of the failure
     */
    public ProcessManagementException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Creates a new ProcessManagementException with the specified cause.
     *
     * @param cause the underlying cause of the failure
     */
    public ProcessManagementException(Throwable cause) {
        super(cause);
    }
}